class activity_rule {
    const TABLE = 'local_rollover_activityrules';

    /** @var moodle_database */
    private $db;

    public function __construct() {
        global $DB;
        $this->db = $DB;
    }

    public function create($rule) {
        $rule->id = $this->db->insert_record(self::TABLE, $rule);
    }
}
